package LA3_Q2;

public class MyPoint {
    private int x;
    private int y;
    public MyPoint(int x,int y)
    {
        this.x = x;
        this.x = y;
    }
    public void setX(int x)
    {
        this.x = x;
    }
    public int getX()
    {
        return x;
    }
    public void setY(int y)
    {
        this.y = y;
    }
    public int getY()
    {
        return y;
    }
}
