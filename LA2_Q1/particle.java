public class Particle {
    private int x;
    private int y;

    public Particle (int inX, int inY)
    {
        inX=x;
        inY=y;
    }

    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

} 